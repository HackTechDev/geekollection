    public function findAllForUser($userId)
    {
            return $this->createQueryBuilder('item')
                    ->leftJoin('item.user','itemUser')
                    ->where('itemUser.id = :userId')
                    ->setParameter('userId', $userId)
                    ->getQuery()
                    ->getResult();
